    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblCourses.getSelectedRow();
        
        
        if (selectedRow>-1){   //selectedRow houwe l index taba3 l row
            int crsid = Integer.parseInt(tblCourses.getValueAt(selectedRow, 0).toString());
            try{
                Statement stmt = con.createStatement();
                stmt.execute("Delete From tbl_unicourses where crs_id ="+crsid);
                refreshTable();
            }catch(SQLException ex){
                System.err.println(ex.getMessage());
            }
        }else{
            JOptionPane.showMessageDialog(this,"Select a record to delete","warning",JOptionPane.WARNING_MESSAGE);
        }
    
    }//GEN-LAST:event_btnDeleteActionPerformed

