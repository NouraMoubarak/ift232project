 private void refreshTable(){
          uni_coursesPUEntityManager.getTransaction().begin();
          java.util.Collection data = tblUnicoursesQuery.getResultList();
          for(Object entity : data){
              uni_coursesPUEntityManager.refresh(entity);
          }
          tblUnicoursesList.clear();
          tblUnicoursesList.addAll(data);
          uni_coursesPUEntityManager.getTransaction().commit();
          bindingGroup.unbind();
          bindingGroup.bind();
          tblCourses.getColumnModel().getColumn(0).setMinWidth(0);
          tblCourses.getColumnModel().getColumn(0).setMaxWidth(0);
      }
